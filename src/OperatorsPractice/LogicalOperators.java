package OperatorsPractice;

public class LogicalOperators {
    public static void main(String[] args) {

        // && - AND
        boolean a = true;
        boolean b = true;

        System.out.println(a && b );


        // || - OR
        boolean c = false;
        boolean d = false;

        System.out.println(c || d);


        // ! - NOT

        boolean e = false;
        System.out.println(!e);

    }
}
