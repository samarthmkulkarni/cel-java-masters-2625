package OperatorsPractice;

public class BitwiseOperators {
    public static void main(String[] args) {

        // & - Bitwise AND
        int a = 5;
        int b = 3;

        System.out.println(a & b);


        // | - Bitwse OR

        System.out.println(a | b );

        // ~ (tilde) Bitwise Not

        System.out.println(~a);

        // << Left Shift
        int c = 5;
        System.out.println(c<<2);

        // >> Right Shift

        System.out.println(a>>2);



    }
}
