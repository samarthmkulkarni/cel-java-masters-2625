package OperatorsPractice;

public class AssignemtOperators
{
    public static void main(String[] args)
    {

        //assignment operator
        int a = 45;
        System.out.println(a);

        // +=
        a +=2;   // a = a+2
        System.out.println(a);

        // -=
        a-=2;   // a = a-2;
        System.out.println(a);

        // *=
        int b = 10;
        b *= 2;   // b = b*2
        System.out.println(b);

        // /=
        int c = 10;
        c/=2;   //c = c/2;
        System.out.println(c);

        // %=
        int d = 10;
        d%=2;
        System.out.println(d);

    }


}