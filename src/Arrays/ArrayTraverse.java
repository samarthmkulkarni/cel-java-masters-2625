package Arrays;

public class ArrayTraverse {
    public static void main(String[] args) {

        // Traversing Using Loops
        //                0  1  2  3  4   5
        int[] arrayOne = {10, 20, 30, 40, 50};

        for (int k = 0; k <= 4; k++)  // 5 <=4 (False)
        {
            System.out.print(arrayOne[k]);
        }
    }
}