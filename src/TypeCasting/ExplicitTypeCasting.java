package TypeCasting;

public class ExplicitTypeCasting {

    public static void main(String[] args) {

        // ExplicitTypeCasting
        // Bigger to small

        // Data loss possible
        // Extra Syntax

        // targetType varible = (targetData Type) value



        // double to int

        double d = 9.99999;
        int i = (int) d;

        System.out.println(i);

        // float to long
        float f = 123.4534543f;

        long l = (long) f;
        System.out.println(l);


        // int to char
        int num = 65;
        char c = (char)num;
        System.out.println(c);




    }
}
